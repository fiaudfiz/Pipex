#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t			i;
	unsigned char	*s3;
	unsigned char	*s4;

	i = 0;
	s3 = (unsigned char *)s1;
	s4 = (unsigned char *)s2;
	while (i < n && s3[i] == s4[i])
		i++;
	if (i == n)
		return (0);
	return ((int)(s3[i] - s4[i]));
}
